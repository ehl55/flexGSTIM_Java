    /**
     * Called when user pressed "Connect" button. Attempts to connect (or disconnect)
     * to selected COMM port.
     * @param evt 
     */
    private void connectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectActionPerformed

        if(connect.getText().equals("Connect")) {
            // attempt to connect to the serial port
            chosenPort = SerialPort.getCommPort(selectCOMM.getSelectedItem().toString());
            chosenPort.setComPortTimeouts(SerialPort.TIMEOUT_SCANNER, 0, 0);
            
            if(chosenPort.openPort()) {
                //update chosenPort in MainWindow
                mainWin.setChosenPort(chosenPort);
                
                //delete this window
                this.dispose();
            }
        } else {
            //update chosenPort in MainWindow
            mainWin.setChosenPort(null);
            
            // disconnect from the serial port
            chosenPort.closePort();
            selectCOMM.setEnabled(true);
            connect.setText("Connect");
        }
    }//GEN-LAST:event_connectActionPerformed

