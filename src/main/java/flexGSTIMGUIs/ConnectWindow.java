    /**
     * Populates the selectCOMM JComboBox with COMM port options
     */
    private void updateSelectCOMM() {
	SerialPort[] portNames = SerialPort.getCommPorts();
	for(int i = 0; i < portNames.length; i++) {
            selectCOMM.addItem(portNames[i].getSystemPortName());
        }     
    }
    
    /**
     * Updates connect JButton with proper text and enables/disables accordingly
     */
    private void updateButtons() {
        if(chosenPort!=null && chosenPort.openPort()) {//check if already connected
            connect.setText("Disconnect");
            selectCOMM.setSelectedItem(chosenPort.getSystemPortName());
            selectCOMM.setEnabled(false);
        } 
    }

